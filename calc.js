// Persegi

exports.luasPersegi = (p, l) => {
  let luas = p * l;
  return luas;
};

exports.KelPersegi = (p, l) => {
  let keliling = 2 * (p + l);
  return keliling;
};

// // Persegi Panjang

exports.luasPP = (p, l) => {
  let luas = p * l;
  return luas;
};

exports.KelPP = (p, l) => {
  let keliling = 2 * (p + l);
  return keliling;
};
